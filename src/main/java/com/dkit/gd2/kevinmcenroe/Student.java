package com.dkit.gd2.kevinmcenroe;

public class Student {
    String name;
    String id;
    String email;
    int telephone;
    String[] computersOnLoan;

    public Student(String name, String id, String email, int telephone, String[] computersOnLoan) {
        this.name = name;
        this.id = id;
        this.email = email;
        this.telephone = telephone;
        this.computersOnLoan = computersOnLoan;
    }
}
